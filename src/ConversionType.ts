/**
 * Represents a conversion-type.
 */
enum ConversionType
{
    /**
     * BMP-conversion.
     */
    BMP,
    /**
     * HTML-conversion.
     */
    HTML,

    /**
     * JPG-conversion
     */
    JPEG,
    /**
     * PDF-conversion.
     */
    PDF,

    /**
     * PNG-conversion.
     */
    PNG,

    /**
     * PPM-conversion.
     */
    PPM
}

export default ConversionType;