/**
 * Represents a standardized format.
 */
enum StandardizedFormatType
{
    /**
     * Indicates the A3-format.
     */
    A3,

    /**
     * Indicates the A4-format.
     */
    A4,

    /**
     * Indicates the A5-format.
     */
    A5,

    /**
     * Indicates the Legal-format.
     */
    Legal,

    /**
     * Indicates the Letter-format.
     */
    Letter,

    /**
     * Indicates the Tabloid-format.
     */
    Tabloid
}

export default StandardizedFormatType;